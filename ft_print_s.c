#include "ft_printf.h"

void	ft_put_s(char *s, int *count)
{
	int	i;

	i = 0;
	if (s == NULL)
		s = "(null)";
	while (s[i])
	{
		write(1, &s[i], 1);
		i++;
		*count = *count + 1;
	}
}
