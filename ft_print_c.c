#include "ft_printf.h"

void	ft_putchar_count(char c, int *count)
{
	*count = *count + 1;
	write(1, &c, 1);
}

void	ft_putstr(char *s)
{
	int	i;

	i = 0;
	while (s[i])
	{
		write(1, &s[i], 1);
		i++;
	}
}

void	ft_put_c(char c, int *count)
{
	write(1, &c, 1);
	*count = *count + 1;
}
